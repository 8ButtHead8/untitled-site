/* ─── Branching ─── */

function getNextId(currentId: string, answers: Record<string, string>): string | null {
  switch (currentId) {
    case "grade": return "trigger";
    case "trigger": return "child_reaction";
    case "child_reaction":
      return (answers.child_reaction || "").includes("Злится") ? "conflict_level" : "interests";
    case "conflict_level": return "interests";
    case "interests": {
      const v = answers.interests || "";
      return v.includes("Похоже") || v.includes("Не знаю") ? "where_energy" : "already_tried";
    }
    case "where_energy": return "already_tried";
    case "already_tried": return "time_pressure";
    case "time_pressure": return "success_criteria";
    case "success_criteria": return "parent_role";
    case "parent_role": return null;
    default: return null;
  }
}

function getEstimatedTotal(answers: Record<string, string>): number {
  let total = 9;
  if ((answers.child_reaction || "").includes("Злится")) total += 1;
  const v = answers.interests || "";
  if (v.includes("Похоже") || v.includes("Не знаю")) total += 1;
  return total;
}

/* ─── Mirror builder ─── */

function buildMirror(segment: string, answers: Record<string, string>): string {
  const grade = answers.grade || "";
  const childReaction = answers.child_reaction || "";
  const alreadyTried = answers.already_tried || "";
  const interests = answers.interests || "";

  const gradeLabel = ["9 класс", "10 класс", "11 класс"].includes(grade) ? grade : null;
  const triedTests = alreadyTried.includes("Онлайн-тесты");
  const triedMany = alreadyTried.includes("Пробовали многое");
  const triedNothing = alreadyTried.includes("Пока ничего");

  switch (segment) {
    case "urgent": {
      const opening = gradeLabel
        ? `${gradeLabel} — это когда вопрос "куда поступать" перестаёт быть абстрактным и становится срочным.`
        : "Времени почти нет — и это давит.";
      const reaction = childReaction.includes("Злится")
        ? "При этом поднять тему дома — уже риск: ребёнок закрывается или реагирует с раздражением."
        : childReaction.includes("«не знаю»")
        ? 'В ответ на любой вопрос — "не знаю", и разговор снова заходит в никуда.'
        : "Ребёнок, может, и хочет разобраться — но как это сделать, непонятно ни ему, ни вам.";
      const tried = triedTests
        ? "Тесты пробовали — список профессий появился, а ясности не прибавилось."
        : triedMany
        ? "Перепробовали уже немало — но результат один и тот же: непонятно что дальше."
        : "";
      return [opening, reaction, tried, "Время идёт, а точки опоры всё нет."].filter(Boolean).join(" ");
    }

    case "closed": {
      const tried = triedTests
        ? "Тесты пробовали — но заставить ребёнка воспринять их серьёзно не вышло."
        : triedMany
        ? "Перепробовали уже немало — но каждый раз одно и то же."
        : triedNothing
        ? "Не знаете с чего начать, чтобы не сделать хуже."
        : "И уже не знаете, как подступиться без нового напряжения.";
      return `Вы хотите помочь — это видно. Но любая попытка поговорить о будущем заканчивается одинаково: ребёнок закрывается, злится или уходит от темы. ${tried} Со временем начинаешь обходить эту тему стороной — потому что зачем снова создавать напряжение.`;
    }

    case "no_interests": {
      const tried = triedTests
        ? "Тесты прошли — но они только подтвердили: ничего не зацепило."
        : triedNothing
        ? "Пока даже не знаете с чего начать."
        : "";
      const noClarity = interests.includes("Не знаю")
        ? "Понять, что вообще ему нравится — само по себе загадка."
        : "Яркого «загорания» нет нигде.";
      return `«Ничего не интересует» — это, пожалуй, самый пугающий ответ для родителя. Потому что если нет интересов — что вообще искать? ${noClarity} ${tried} Кажется, что ребёнок просто равнодушен ко всему — или что вы что-то упустили.`.replace(/  +/g, " ").trim();
    }

    case "wrong_direction":
      return `Ребёнок знает чего хочет — но его выбор вас беспокоит. "На это не проживёшь", "это несерьёзно", "он просто не понимает реальности". При этом давить не хочется — но и просто согласиться страшно. Вы оказываетесь между двух огней: поддержать или направить в другую сторону.`;

    case "in_advance": {
      const gradePart = gradeLabel ? ` Вашему ребёнку ${gradeLabel} — ` : " ";
      return `Вы думаете об этом заранее — и это уже большой плюс. Большинство семей приходят ко мне, когда давление максимально, а времени минимально.${gradePart}есть пространство для настоящего разговора, без спешки и паники. Это другой уровень работы.`;
    }

    default:
      return `Судя по вашим ответам, ситуация сложная — но не безвыходная. Есть запрос, есть материал для работы, есть желание разобраться. Именно с этого и начинается хорошая профориентация.`;
  }
}

