/* ─── Branching ─── */

function getNextId(currentId: string, answers: Record<string, string>): string | null {
  switch (currentId) {
    case "situation":
      return (answers.situation || "").includes("Ищу работу") ? "job_search_state" : "main_pain";
    case "job_search_state": return "main_pain";
    case "main_pain": return "blocker";
    case "blocker": return "attempts";
    case "attempts": return "time_horizon";
    case "time_horizon": return "desired_outcome";
    case "desired_outcome": return "readiness";
    case "readiness": return null;
    default: return null;
  }
}

function getEstimatedTotal(answers: Record<string, string>): number {
  let total = 8;
  if ((answers.situation || "").includes("Ищу работу")) total += 1;
  return total;
}

/* ─── Mirror builder ─── */

function buildMirror(segment: string, answers: Record<string, string>): string {
  const mainPain = answers.main_pain || "";
  const blocker = answers.blocker || "";
  const attempts = answers.attempts || "";
  const jobSearchState = answers.job_search_state || "";

  const triedSameSphere = attempts.includes("той же сфере");
  const triedReadLots = attempts.includes("Читал");
  const neverStarted = attempts.includes("не решался");
  const firstTime = attempts.includes("первая попытка");

  switch (segment) {
    case "too_late": {
      const financePart = blocker.includes("Финансовые")
        ? " Плюс финансовые обязательства — они делают любой риск особенно страшным."
        : "";
      const attemptPart = triedSameSphere
        ? " Меняли работу в той же сфере — но стало так же."
        : neverStarted
        ? " Думаете об этом — но никак не можете начать."
        : "";
      return `«Уже поздно» — это одна из самых тяжёлых мыслей, потому что она звучит как приговор. Вы видите, как молодые специалисты быстро растут — и думаете: окно закрылось.${financePart}${attemptPart} А попытка даже подумать о переменах кажется безответственной.`;
    }

    case "burnout": {
      const painPart = mainPain.includes("усталость")
        ? " Истощение стало фоном — утром уже без сил, а день ещё не начался."
        : mainPain.includes("Нет роста")
        ? " Нет движения вперёд — и непонятно, будет ли оно вообще."
        : mainPain.includes("Деньги")
        ? " Отдаёте больше, чем получаете — и с каждым месяцем это очевиднее."
        : "";
      const attemptPart = triedSameSphere
        ? " Меняли работу внутри той же сферы — но легче не стало."
        : triedReadLots
        ? " Читали, смотрели, искали ответ — но он не собирается в план."
        : "";
      return `Вы выгораете — и при этом продолжаете тянуть. Утром не хочется вставать, но надо.${painPart}${attemptPart} А самое неприятное: непонятно, это временно или так будет всегда. Чем дольше это длится — тем сложнее понять, то ли место не то, то ли профессия, то ли что-то ещё.`;
    }

    case "job_search": {
      const dirPart = jobSearchState.includes("Полностью")
        ? " При этом хочется полностью сменить направление — но куда именно, непонятно."
        : jobSearchState.includes("смежную")
        ? " Хочется перейти в смежную область — но как выбрать и с чего начать, неясно."
        : jobSearchState.includes("не понимаю")
        ? " И даже направление пока непонятно — слишком много вопросов без ответов."
        : "";
      return `Поиск работы без понимания куда — это особый вид давления. Каждый день без оффера ощущается как потеря времени. Хочется схватить первое подходящее предложение, лишь бы вопрос закрылся.${dirPart} Но внутри есть ощущение: если выбрать наугад — через год всё начнётся сначала.`;
    }

    case "no_clarity": {
      const attemptPart = triedReadLots
        ? " Читали много — варианты появились, но туман не рассеялся."
        : neverStarted
        ? " Думаете об этом — но никак не можете решиться начать."
        : firstTime
        ? " Это первая попытка всерьёз разобраться."
        : "";
      return `Смешалось всё: что умеете, что хотите, что вообще возможно. Хочется ясности — но непонятно куда смотреть.${attemptPart} И чем больше информации, тем сложнее: вариантов много, а определённости нет.`;
    }

    case "empty": {
      const painPart = mainPain.includes("Нет смысла")
        ? " Смысла нет — и это сложно объяснить, потому что объективных причин жаловаться нет."
        : mainPain.includes("Страшно")
        ? " И внутри тихий страх: что если так будет всегда?"
        : "";
      return `Объективно всё хорошо: работа есть, платят нормально, жить можно. Но внутри пусто.${painPart} Говорить об этом сложно — кажется, что несерьёзно, что «надо просто радоваться». Но ощущение никуда не уходит, и с каждым годом оно становится тяжелее.`;
    }

    default:
      return `Вы в точке, где хочется изменений — и это уже важно. Есть осознание что что-то не так, есть желание разобраться. Осталось превратить это в конкретный план.`;
  }
}

